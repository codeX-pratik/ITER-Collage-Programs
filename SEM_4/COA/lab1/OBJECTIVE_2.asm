; SWAPPING OF NIBBLE OF DATA1                      
MOV SI,1000H
MOV AL,[SI]
MOV BL,[SI]
INC SI
SHR AL,04H
SHL BL,04H
OR AL,BL
INC SI
MOV [SI],AL

; Y = (DATA1 AND DATA2) OR (DATA1 XOR DATA2)
MOV AL,[1000H]
MOV BL,[1001H]
MOV CL,AL
AND AL,BL
XOR BL,CL
OR AL,BL
INC SI
MOV [SI],AL

